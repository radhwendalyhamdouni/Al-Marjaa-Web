use serde::Serialize;
use serde_json::{json, Value as JsonValue};

/// نوع JSON للاستخدام السهل
pub type Json = JsonValue;

/// إنشاء JSON بسهولة
/// 
/// # مثال
/// ```rust
/// let data = json_عربي! {
///     "الاسم" => "أحمد",
///     "العمر" => 25
/// };
/// ```
#[macro_export]
macro_rules! json_عربي {
    ($($key:expr => $value:expr),* $(,)?) => {
        {
            let mut map = serde_json::Map::new();
            $(
                map.insert($key.to_string(), serde_json::to_value($value).unwrap());
            )*
            serde_json::Value::Object(map)
        }
    };
}

