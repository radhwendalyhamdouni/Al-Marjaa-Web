/// نوع الوسيطة
pub type نوع_وسیطة = Arc<dyn Fn(طلب) -> Option<رد> + Send + Sync>;

// ═══════════════════════════════════════════════════════════════════════════════
// سلسلة الوسائط
// ═══════════════════════════════════════════════════════════════════════════════

/// سلسلة الوسائط
pub struct سلسلة_الوسائط {
    الوسائط: Vec<نوع_وسیطة>,
}

impl سلسلة_الوسائط {
    /// إنشاء سلسلة جديدة
    pub fn جديد() -> Self {
        سلسلة_الوسائط {
            الوسائط: Vec::new(),
        }
    }
    
    /// إضافة وسیطة
    pub fn أضف<F>(&mut self, المعالج: F)
    where
        F: Fn(طلب) -> Option<رد> + Send + Sync + 'static,
    {
        self.الوسائط.push(Arc::new(المعالج));
    }
    
    /// تنفيذ الوسائط
    pub fn نفذ(&self, طلب_م: طلب) -> Option<رد> {
        for middleware in &self.الوسائط {
            if let Some(response) = middleware(طلب_م.clone()) {
                return Some(response);
            }
        }
        None
    }
    
    /// عدد الوسائط
    pub fn عدد(&self) -> usize {
        self.الوسائط.len()
    }
}

impl Default for سلسلة_الوسائط {
    fn default() -> Self {
        سلسلة_الوسائط::جديد()
    }
}

// ═══════════════════════════════════════════════════════════════════════════════
// الوسيطة
// ═══════════════════════════════════════════════════════════════════════════════

/// وسیطة جاهزة
pub struct وسیطة;

impl وسیطة {
    // ─────────────────────────────────────────────────────────────────────
    // التسجيل (Logging)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة تسجيل الطلبات
    pub fn تسجيل() -> نوع_وسیطة {
        Arc::new(|طلب_م: طلب| {
            let now = chrono::Local::now().format("%Y-%m-%d %H:%M:%S");
            println!("[{}] {} {} - {:?}", 
                now, 
                طلب_م.الطريقة, 
                طلب_م.المسار,
                طلب_م.ip_العميل()
            );
            None
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // الأمان (Security)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة CORS
    pub fn cors() -> نوع_وسیطة {
        Arc::new(|_: طلب| {
            None // CORS يُضاف في الرد
        })
    }
    
    /// وسیطة التحقق من الأصل
    pub fn التحقق_من_الأصل(الأصول_المسموحة: Vec<&str>) -> نوع_وسیطة {
        let origins: Vec<String> = الأصول_المسموحة.iter().map(|s| s.to_string()).collect();
        Arc::new(move |طلب_م: طلب| {
            if let Some(origin) = طلب_م.رأس("Origin") {
                if !origins.contains(&origin.to_string()) {
                    return Some(رد::محظور("الأصل غير مسموح"));
                }
            }
            None
        })
    }
    
    /// وسیطة التحقق من التوكن
    pub fn التحقق_من_التوكن(الرأس: &str) -> نوع_وسیطة {
        let header = الرأس.to_string();
        Arc::new(move |طلب_م: طلب| {
            if طلب_م.رأس(&header).is_none() {
                return Some(رد::غير_مصرح("التوكن مطلوب"));
            }
            None
        })
    }
    
    /// وسیطة Bearer Token
    pub fn bearer_token() -> نوع_وسیطة {
        Arc::new(|طلب_م: طلب| {
            if طلب_م.رمز_الحامل().is_none() {
                return Some(رد::غير_مصرح("رمز Bearer مطلوب"));
            }
            None
        })
    }
    
    /// وسیطة API Key
    pub fn api_key(المفتاح: &str) -> نوع_وسیطة {
        let key = المفتاح.to_string();
        Arc::new(move |طلب_م: طلب| {
            let provided = طلب_م.رأس("X-API-Key")
                .or_else(|| طلب_م.استعلام("api_key"));
            
            match provided {
                Some(k) if k == &key => None,
                _ => Some(رد::غير_مصرح("مفتاح API غير صالح")),
            }
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // حدود الاستخدام (Rate Limiting)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة حد الطلبات (بسيطة)
    pub fn حد_الطلبات(الحد: u32) -> نوع_وسیطة {
        use std::sync::atomic::{AtomicU32, Ordering};
        let counter = Arc::new(AtomicU32::new(0));
        let limit = الحد;
        
        Arc::new(move |_: طلب| {
            let current = counter.fetch_add(1, Ordering::Relaxed);
            if current >= limit {
                Some(رد::خطأ("تم تجاوز حد الطلبات", 429))
            } else {
                None
            }
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // المحتوى (Content)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة حد حجم المحتوى
    pub fn حد_الحجم(الحجم_بالبايت: usize) -> نوع_وسیطة {
        let size = الحجم_بالبايت;
        Arc::new(move |طلب_م: طلب| {
            if طلب_م.حجم_المحتوى() > size {
                return Some(رد::خطأ("المحتوى كبير جداً", 413));
            }
            None
        })
    }
    
    /// وسیطة JSON فقط
    pub fn json_فقط() -> نوع_وسیطة {
        Arc::new(|طلب_م: طلب| {
            if !طلب_م.إرسال() {
                return None;
            }
            
            match طلب_م.نوع_المحتوى() {
                Some(ct) if ct.contains("application/json") => None,
                _ => Some(رد::خطأ("المحتوى يجب أن يكون JSON", 415)),
            }
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // التحول (Transformation)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة إضافة رأس
    pub fn أضف_رأس(الاسم: &str, القيمة: &str) -> نوع_وسیطة {
        let name = الاسم.to_string();
        let value = القيمة.to_string();
        Arc::new(move |_: طلب| {
            None // الرأس يُضاف في الرد
        })
    }
    
    /// وسیطة إضافة رأس الخادم
    pub fn رأس_الخادم() -> نوع_وسیطة {
        Arc::new(|_: طلب| {
            None // الرأس يُضاف في الرد
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // التوجيه (Routing)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة إعادة التوجيه
    pub fn إعادة_توجيه(من: &str, إلى: &str) -> نوع_وسیطة {
        let from = من.to_string();
        let to = إلى.to_string();
        Arc::new(move |طلب_م: طلب| {
            if طلب_م.المسار == from {
                return Some(رد::توجه(&to));
            }
            None
        })
    }
    
    /// وسیطة إعادة التوجيه إلى HTTPS
    pub fn توجيه_https() -> نوع_وسیطة {
        Arc::new(move |طلب_م: طلب| {
            // التحقق من X-Forwarded-Proto أوscheme
            if let Some(proto) = طلب_م.رأس("X-Forwarded-Proto") {
                if proto == "http" {
                    if let Some(host) = طلب_م.المضيف() {
                        let https_url = format!("https://{}{}", host, طلب_م.المسار);
                        return Some(رد::توجه(&https_url));
                    }
                }
            }
            None
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // الصيانة (Maintenance)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة وضع الصيانة
    pub fn صيانة(الرسالة: &str) -> نوع_وسیطة {
        let message = الرسالة.to_string();
        Arc::new(move |_: طلب| {
            Some(رد::خطأ(&message, 503))
        })
    }
    
    /// وسیطة الصيانة مع استثناءات
    pub fn صيانة_مع_استثناء(الرسالة: &str, IPs_المسموحة: Vec<&str>) -> نوع_وسیطة {
        let message = الرسالة.to_string();
        let allowed_ips: Vec<String> = IPs_المسموحة.iter().map(|s| s.to_string()).collect();
        
        Arc::new(move |طلب_م: طلب| {
            // التحقق من IP
            if let Some(ip) = طلب_م.ip_العميل() {
                let ip_str = ip.to_string();
                if allowed_ips.contains(&ip_str) || ip.is_loopback() {
                    return None;
                }
            }
            
            Some(رد::خطأ(&message, 503))
        })
    }
    
    // ─────────────────────────────────────────────────────────────────────
    // الضغط (Compression)
    // ─────────────────────────────────────────────────────────────────────
    
    /// وسیطة الضغط
    pub fn ضغط() -> نوع_وسیطة {
        Arc::new(|_: طلب| {
            None // الضغط يُطبق على الرد
        })
    }
}
